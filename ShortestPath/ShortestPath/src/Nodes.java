
public class Nodes {
	
		private int x;
		private int y;
		private int id;
		
		public Nodes(int x, int y, int id) {
			this.x=x;
			this.y=y;
			this.id=id;
		}
		
		public void setX(int x) {
			this.x=x;
		}
		
		public void setY(int y) {
			this.y=y;
		}
		
		public int getX() {
			return x;
		}
		
		public int getY() {
			return y;
		}
		
		public void setIndex(int id) {
			this.id=id;
		}
		
		public int getIndex() {
			return id;
		}
		
}
